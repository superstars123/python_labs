from model import Player

# =========================================================
# СЦЕНАРИЙ 1: Инициализация и базовые проверки
# =========================================================
print("\n=== Сценарий 1: Инициализация и проверки ===")

# Создание игроков с разными параметрами
p1 = Player("Vika", 100, 1, 0)
p2 = Player("Masha", 90, 1, 10)
p3 = Player("Dasha", 120, 2, 50)

players = [p1, p2, p3]

print("\nСписок игроков:")
for i, p in enumerate(players, start=1):
    print(f"{i}. {p}")

# Попытки создать некорректного игрока
print("\nПроверка ошибок:")
cases = [
    ("", 100, 1, 0),
    ("Test", -1, 1, 0),
    ("Test", 100, 0, 0),
    ("Test", 100, 1, -10),
]

for case in cases:
    try:
        Player(*case)
    except Exception as e:
        print(f"Ошибка для {case}: {e}")


# =========================================================
# СЦЕНАРИЙ 2: Взаимодействие игроков
# =========================================================
print("\n=== Сценарий 2: Взаимодействие и состояние ===")

# Игроки получают урон
print("\nБой начинается:")
p1.take_damage(20)
p2.take_damage(40)
p3.take_damage(10)

for p in players:
    print(p, "| Активен:", p.active)

# Попытка "перелечить" или задать вручную
print("\nИзменение здоровья:")
p1.health = 95
print("После лечения:", p1)

# Ошибка при некорректном значении
try:
    p2.health = -200
except Exception as e:
    print("Ошибка:", e)

# Опыт
print("\nИгроки получают опыт:")
for p in players:
    p.gain_experience(120)
    print(p)


# =========================================================
# СЦЕНАРИЙ 3: Критические состояния
# =========================================================
print("\n=== Сценарий 3: Критические состояния ===")

# Доводим игрока до смерти
print("\nФатальный урон:")
p2.take_damage(1000)
print(p2, "| Активен:", p2.active)

# Попытка действия мёртвого
print("\nПопытка прокачки мёртвого игрока:")
try:
    p2.gain_experience(50)
except Exception as e:
    print("Ошибка:", e)

# Воскрешение с последующей проверкой
print("\nВоскрешение игрока:")
p2.revive()
print(p2)

# Проверка, что снова можно действовать
p2.gain_experience(50)
print("После опыта:", p2)

# =========================================================
# СЦЕНАРИЙ 4: Магия Python
# =========================================================
print("\n=== Сценарий 4: Магические методы ===")

print("\nrepr (для разработчика):")
for p in players:
    print(repr(p))

print("\nstr (для пользователя):")
for p in players:
    print(p)

# Сравнение
print("\nСравнение игроков:")
print("Vika и Masha:", p1 == p2)
print("Masha и Dasha:", p2 == p3)

# Сортировка по уровню / логике __lt__
print("\nОтсортированный список:")
for p in sorted(players):
    print(p)

# Атрибуты класса
print("\nПроверка атрибутов класса:")
print("MAX_LEVEL:", Player.MAX_LEVEL)

print("\nДоступ через экземпляры:")
for p in players:
    print(f"{p.name} видит MAX_LEVEL =", p.MAX_LEVEL)
